export function getDocumentContent(docId: string): DocumentContent | null {
  const doc = ALL_DOCUMENTS.find((d) => d.id === docId);
  if (!doc) return null;
  const authored = AUTHORED_PAGES[docId] ?? [];
  const byPage = new Map(authored.map((p) => [p.page, p]));
  const pages: DocumentPage[] = [];
  for (let p = 1; p <= doc.pageCount; p++) {
    pages.push(byPage.get(p) ?? generatedPage(p, doc.name));
  }
  const toc = [
    { label: "الديباجة", page: 1 },
    { label: "صلب المستند", page: Math.max(1, Math.ceil(doc.pageCount / 3)) },
    {
      label: "الطلبات والخاتمة",
      page: Math.max(1, doc.pageCount - 1),
    },
  ];
  return { docId, pages, tableOfContents: toc };
}
